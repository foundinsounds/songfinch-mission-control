// Slack Incoming Webhook Notifier — Pushes pipeline events to Slack
// Uses Block Kit for rich message formatting
// Non-blocking: all calls are fire-and-forget, never stalls the pipeline

const SLACK_WEBHOOK_URL = process.env.SLACK_WEBHOOK_URL

const AGENT_EMOJI = {
  CMO: '👔', CHIEF: '🎖️', MUSE: '🎨', HOOK: '🪝', PULSE: '📱',
  LENS: '🎬', STORY: '📖', SCOUT: '🔭', FLOW: '🌊', PIXEL: '🖼️',
  System: '⚙️',
}

const STATUS_COLOR = {
  approved: '#4ade80',    // green
  revised: '#f59e0b',     // amber
  planned: '#818cf8',     // indigo
  completed: '#22d3ee',   // cyan
  error: '#ef4444',       // red
  info: '#64748b',        // slate
  image: '#c084fc',       // purple
}

// ── MAIN SENDER ─────────────────────────────────

async function sendSlack(blocks, text = 'Mission Control Update') {
  if (!SLACK_WEBHOOK_URL) return // Silently skip if not configured

  try {
    await fetch(SLACK_WEBHOOK_URL, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ text, blocks }),
    })
  } catch {
    // Non-blocking — pipeline never stalls for Slack
  }
}

// ── EVENT-SPECIFIC NOTIFIERS ─────────────────────

// Content approved by CHIEF
export async function notifyApproved({ task, agent, score, summary, imageGenerated }) {
  const emoji = AGENT_EMOJI[agent] || '🤖'
  const scoreBar = score >= 4.0 ? '🟢' : score >= 3.0 ? '🟡' : '🔴'

  const blocks = [
    {
      type: 'section',
      text: {
        type: 'mrkdwn',
        text: `✅ *Content Approved*\n*${task}*\n${emoji} ${agent} → ${scoreBar} ${score}/5`,
      },
    },
    summary && {
      type: 'context',
      elements: [{ type: 'mrkdwn', text: summary.substring(0, 200) }],
    },
    imageGenerated && {
      type: 'context',
      elements: [{ type: 'mrkdwn', text: '🖼️ Visual companion auto-generated by PIXEL' }],
    },
  ].filter(Boolean)

  await sendSlack(blocks, `✅ Approved: ${task} (${score}/5)`)
}

