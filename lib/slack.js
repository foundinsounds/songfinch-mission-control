// Pipeline health alert
export async function notifyPipelineAlert({ type, message, details }) {
  const icon = type === 'stall' ? '🚨' : type === 'low_queue' ? '⚠️' : 'ℹ️'

  const blocks = [
    {
      type: 'section',
      text: {
        type: 'mrkdwn',
        text: `${icon} *Pipeline Alert: ${type.toUpperCase()}*\n${message}`,
      },
    },
    details && {
      type: 'context',
      elements: [{ type: 'mrkdwn', text: details.substring(0, 300) }],
    },
  ].filter(Boolean)

  await sendSlack(blocks, `${icon} Alert: ${message}`)
}

// Daily digest summary
export async function notifyDailyDigest({ totalTasks, completed, inProgress, quality, topAgent, velocity }) {
  const blocks = [
    {
      type: 'section',
      text: {
        type: 'mrkdwn',
        text: [
          '📊 *Daily Roundtable Digest*',
          `━━━━━━━━━━━━━━━━━━━━━`,
          `📋 Total Tasks: *${totalTasks}* | ✅ Completed: *${completed}*`,
          `🔄 In Progress: *${inProgress}*`,
          quality ? `⭐ Avg Quality: *${quality}/5*` : null,
          velocity ? `🚀 Velocity: *${velocity}/day*` : null,
          topAgent ? `🏆 Top Agent: *${topAgent}*` : null,
        ].filter(Boolean).join('\n'),
      },
    },
  ]

  await sendSlack(blocks, `Daily digest: ${completed} completed, quality ${quality}/5`)
}
