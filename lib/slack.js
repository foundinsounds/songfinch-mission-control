// Cron pipeline cycle complete
export async function notifyCronCycle({ processed, assigned, planned, reviewed, errors, duration }) {
  const blocks = [
    {
      type: 'section',
      text: {
        type: 'mrkdwn',
        text: [
          '⚙️ *Pipeline Cycle Complete*',
          `📝 Processed: *${processed}* | 📋 Assigned: *${assigned}*`,
          planned > 0 ? `📊 Planned: *${planned}* new tasks` : null,
          reviewed > 0 ? `🎖️ Reviewed: *${reviewed}*` : null,
          errors > 0 ? `❌ Errors: *${errors}*` : null,
          `⏱️ ${duration}`,
        ].filter(Boolean).join('\n'),
      },
    },
  ]

  await sendSlack(blocks, `Pipeline: ${processed} processed, ${assigned} assigned`)
}

